package com.company.java006_ex;

public class Repaet014_4 {
	public static void main(String[] args) {
		// for
			System.out.println("for문 출력");
		for (int i = 1; i <= 5; i++) {
			System.out.print(i + " ");
		}
		System.out.println();
		// while
		System.out.println("while문 출력");
		int j = 1;
		while (j <= 5)
		{
			System.out.print(j + " ");
			j++;
		}
		System.out.println();
		// do while
		System.out.println("do while문 출력");
		int k = 1;
		do{
			System.out.print(k + " ");
			k++;
		}while (k <= 5);
	}
}
