package com.company.java007_ex;

public class Repeat015_4 {
	public static void main(String[]args) {
		int [] nums = new int[3];
		int data =10;
		
		//값 넣기
		for(int i=0; i<nums.length; i++)
		{nums[i]=data; data+=10;}
		
		//값 출력
		for(int i=0; i<nums.length; i++)
		{System.out.println(nums[i] + "");}
		
		
		
	}

}
